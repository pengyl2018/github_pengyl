package common;

public class CommonUtil {
    public static void printArray(int[] input) {
        for (int in : input) {
            System.out.print(in + ",");
        }
    }

    public static void printNumber(int input) {
        System.out.println(input);
    }

    public static void printBool(boolean input) {
        System.out.println(input);
    }
}
